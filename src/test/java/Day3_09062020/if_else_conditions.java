package Day3_09062020;

public class if_else_conditions {
    public static void main(String[] args) {

        int a = 2;
        int b = 3;

        if(a<b){

            System.out.println("a is less than b");
        }//end of if statement

        if(a<b){

            System.out.println("a is less than b");
        } else {

            System.out.println("a is not less than b");
        }//end of if/else statement
    }
}
